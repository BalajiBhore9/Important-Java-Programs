package Imp_prog;

public class Integer_to_String_To_Integer 
{
	public static void main(String[] args) 
	{
		// String to Integer
		String str = "5";
		int s = Integer.parseInt(str);
		System.out.println(s);
		
		// Integer to String
		int n = 5;
		String s1 = Integer.toString(n);
		System.out.println(s1);
		
	}

}
